//function to set Cookie
function setCookie(cname, cvalue, exmins) {
    var dt = new Date();
    dt.setTime(dt.getTime() + (exmins * 60 * 1000));
    var expires = "expires=" + dt.toGMTString();
    document.cookie = cname + "=" + cvalue + "; " + expires;
}
